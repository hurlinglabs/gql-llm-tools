import type { TypeIndex, TypeNode, FieldNode, ArgNode } from "./types";
import type { RelevantSchemaInfo } from "./build-lookups";

export interface SizeInfo {
  originalSize: number;
  currentSize: number;
  percentageOfOriginal: string;
  reduction: string;
}

/**
 * Result of retrieving relevant schema types
 * Provides methods to format the schema as SDL or minified SDL
 */
export class SchemaResult {
  private schemaInfo: RelevantSchemaInfo;
  private typeIndex: TypeIndex;
  private originalSize: number | null;

  constructor(
    schemaInfo: RelevantSchemaInfo,
    typeIndex: TypeIndex,
    originalSize: number | null = null,
  ) {
    this.schemaInfo = schemaInfo;
    this.typeIndex = typeIndex;
    this.originalSize = originalSize;
  }

  /**
   * Get size information for the schema result
   */
  getSizeInfo(): SizeInfo {
    const originalSize = this.originalSize ?? this.getOriginalSizeEstimate();
    const currentLength = this.buildContent().length;
    const percentageOfOriginal =
      originalSize > 0
        ? ((currentLength / originalSize) * 100).toFixed(1)
        : "0";
    const reduction = (100 - parseFloat(percentageOfOriginal)).toFixed(1);

    return {
      originalSize,
      currentSize: currentLength,
      percentageOfOriginal,
      reduction,
    };
  }

  /**
   * Format the relevant schema types as valid SDL string
   * - Primary types: full definition with all fields
   * - Root types (Query/Mutation): only matching fields
   * - Referenced types: listed in header comment (not expanded)
   */
  asSDLString(): string {
    const sizeInfo = this.getSizeInfo();
    const { percentageOfOriginal, reduction } = sizeInfo;

    // Generate content first
    const content = this.buildContent();

    // Build final output with header
    const lines: string[] = [];

    // Credit header
    lines.push(
      `# Summary of schema relevant to your query ${percentageOfOriginal}% of original, (${reduction}% reduction) - generated by @hurling/gql-schema-scout`,
    );
    lines.push("");

    // Add header comment about referenced types
    const { referencedTypes } = this.schemaInfo;
    if (referencedTypes.size > 0) {
      const refCount = referencedTypes.size;
      lines.push(
        `# ⚠ ${refCount} referenced type${refCount > 1 ? "s" : ""} not fully defined.`,
      );
      lines.push(
        `# To see their full definition, call retrieveRelevantSchema() with includeReferences: true`,
      );
      lines.push("");
    }

    lines.push(content);
    return lines.join("\n");
  }

  /**
   * Format the relevant schema types as a minified, single-line SDL string
   */
  asMinified(): string {
    const sizeInfo = this.getSizeInfo();
    const { percentageOfOriginal: pct, reduction } = sizeInfo;

    // Generate content first
    const content = this.buildContent(true);

    // Build final output with header
    const parts: string[] = [];

    // Credit header
    parts.push(
      `# ${pct}% of original (${reduction}% reduction) - @hurling/gql-schema-scout`,
    );

    // Add header comment about referenced types
    const { referencedTypes } = this.schemaInfo;
    if (referencedTypes.size > 0) {
      const refCount = referencedTypes.size;
      parts.push(
        `# ⚠ ${refCount} ref${refCount > 1 ? "s" : ""} not defined (use includeReferences: true)`,
      );
    }

    parts.push(content);
    return parts.join(" ");
  }

  private getOriginalSizeEstimate(): number {
    // This is a best-effort estimate - we count the serialized type index
    // In practice, you'd pass this in if you need exact
    let total = 0;
    for (const typeNode of Array.from(this.typeIndex.values())) {
      total += typeNode.name.length;
      if (typeNode.fields) {
        for (const f of typeNode.fields) {
          total += f.name.length + f.returnType.length;
        }
      }
    }
    return total * 2; // Rough estimate multiplier
  }

  private buildContent(minify = false): string {
    const { primaryTypes, rootTypes, referencedTypes, matchedTokens } =
      this.schemaInfo;
    const lines: string[] = [];

    // Add header comment about referenced types
    if (referencedTypes.size > 0) {
      const refCount = referencedTypes.size;
      lines.push(
        `# ⚠ ${refCount} referenced type${refCount > 1 ? "s" : ""} not fully defined.`,
      );
      lines.push(
        `# To see their full definition, call retrieveRelevantSchema() with includeReferences: true`,
      );
      lines.push("");
    }

    // Helper to check if a field matches the user's tokens
    const fieldMatchesQuery = (fieldName: string): boolean => {
      const fieldLower = fieldName.toLowerCase();
      for (const token of Array.from(matchedTokens)) {
        if (fieldLower.includes(token)) {
          return true;
        }
      }
      return false;
    };

    // Helper to get fields filtered by query tokens
    const getFilteredFields = (typeNode: TypeNode): FieldNode[] => {
      if (!typeNode.fields) return [];
      return typeNode.fields.filter((f) => fieldMatchesQuery(f.name));
    };

    // Helper to get all fields
    const getAllFields = (typeNode: TypeNode): FieldNode[] => {
      return typeNode.fields ?? [];
    };

    // First, output primary types (full definition)
    const allTypes = [
      ...Array.from(primaryTypes).sort(),
      ...Array.from(rootTypes).sort(),
    ];

    for (const typeName of allTypes) {
      const typeNode = this.typeIndex.get(typeName);
      if (!typeNode) continue;

      // Add type description as comment
      if (typeNode.description) {
        lines.push(`"""\n${typeNode.description}\n"""`);
      }

      // Type declaration
      const typeDecl = this.formatTypeDeclaration(typeNode);
      lines.push(typeDecl);

      // Determine which fields to include
      const isRoot = rootTypes.has(typeName);
      const fields = isRoot
        ? getFilteredFields(typeNode)
        : getAllFields(typeNode);

      if (fields.length > 0) {
        lines.push("  {");

        for (const field of fields) {
          const fieldLine = this.formatField(field);
          lines.push(fieldLine);
        }

        lines.push("  }");
      } else if (isRoot) {
        lines.push("  {}");
      }

      lines.push("");
    }

    // Remove trailing empty lines
    while (lines.length > 0 && lines[lines.length - 1] === "") {
      lines.pop();
    }

    return lines.join(minify ? ";" : "\n");
  }

  /**
   * Get the set of type names included in this result
   */
  getTypeNames(): Set<string> {
    return new Set([
      ...Array.from(this.schemaInfo.primaryTypes),
      ...Array.from(this.schemaInfo.rootTypes),
    ]);
  }

  private formatTypeDeclaration(typeNode: TypeNode): string {
    const implementsPart = typeNode.interfaces?.length
      ? ` implements ${typeNode.interfaces.join(" & ")}`
      : "";

    return `type ${typeNode.name}${implementsPart}`;
  }

  private formatField(field: FieldNode): string {
    // Add field description as comment if present
    const description = field.description ? `# ${field.description}\n  ` : "";

    const argsStr =
      field.args.length > 0
        ? `(${field.args.map((a) => this.formatArg(a)).join(", ")})`
        : "";

    return `${description}${field.name}${argsStr}: ${field.returnType}`;
  }

  private formatArg(arg: ArgNode): string {
    const description = arg.description ? `# ${arg.description} ` : "";
    return `${description}${arg.name}: ${arg.type}`;
  }
}
